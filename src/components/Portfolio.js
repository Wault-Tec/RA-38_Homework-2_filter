export class Portfolio extends Component {
    constructor(props) {
        super(props);
        this.state = {
        projects: initialArr,
        /* selected: "All", */
        filtredList: initialArr,
        };
    }

    onSelectFilter = (filter) => {
      let filtredList;
      if (filter === "All") {
        filtredList = this.state.projects;
      } else {
        filtredList = this.state.projects.filter((item)=> item.category === filter) 
      }
      this.setState({
        ...this.state, 
        filtredList: filtredList,
        /* selected: filter */
      })
    }
  
  render() {
    const {filtredList} = this.state;
    return (
      <>
        <Toolbar
          filters={["All", "Websites", "Flayers", "Business Cards"]}
          /* selected={this.state.selected} */
          onSelectFilter={this.onSelectFilter}/>
        <ProjectList projects={filtredList}/>
      </>
    )
  }
}

